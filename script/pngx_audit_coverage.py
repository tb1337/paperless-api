def _print_result(r: AuditResult) -> None:
    if r.schema_only:
        omissions = KNOWN_SCHEMA_OMISSIONS.get(r.model_name, {})
        extras = KNOWN_SCHEMA_EXTRAS.get(r.model_name, {})
        ref_label = "schema"
    else:
        omissions = KNOWN_OMISSIONS.get(r.model_name, {})
        extras = KNOWN_EXTRAS.get(r.model_name, {})
        ref_label = "API"

    has_gaps = bool(r.missing_in_model or r.extra_in_model)

    if r.fetch_error:
        status = f"{RED}FETCH ERROR{RESET}"
    elif r.no_data:
        status = f"{YELLOW}NO DATA{RESET}   (no live data and no typed schema component)"
    elif has_gaps:
        status = f"{RED}GAPS FOUND{RESET}"
    elif r.schema_only:
        status = f"{CYAN}SCHEMA ✓{RESET}   (no live data; verified via OpenAPI schema)"
    else:
        status = f"{GREEN}OK{RESET}"

    print(f"\n{BOLD}{r.label}{RESET} ({DIM}{r.model_name}{RESET})  →  {status}")

    if r.fetch_error:
        print(f"  {RED}  {r.fetch_error}{RESET}")
        return

    if r.no_data:
        print(f"  {DIM}model fields: {sorted(r.model_keys)}{RESET}")
        return

    if r.missing_in_model:
        print(f"  {RED}MISSING IN MODEL{RESET} – {ref_label} field not captured by model:")
        for k in r.missing_in_model:
            print(f"    {RED}✗  {k!r}{RESET}")

    if r.extra_in_model:
        print(f"  {YELLOW}EXTRA IN MODEL{RESET} – model field not present in {ref_label}:")
        for k in r.extra_in_model:
            print(f"    {YELLOW}⚠  {k!r}{RESET}")

    if r.known_omissions:
        print(f"  {DIM}known omissions (intentional, not in model):{RESET}")
        for k in r.known_omissions:
            print(f"    {DIM}·  {k!r}  →  {omissions[k]}{RESET}")

    if r.known_extras:
        print(f"  {DIM}known extras (intentional, not in {ref_label}):{RESET}")
        for k in r.known_extras:
            print(f"    {DIM}·  {k!r}  →  {extras[k]}{RESET}")

    if not has_gaps:
        ref_count = len(r.schema_keys) if r.schema_only else len(r.api_keys)
        print(
            f"  {DIM}all {ref_count} {ref_label} fields covered  "
            f"(+ {len(r.known_omissions)} known omissions, "
            f"{len(r.known_extras)} known extras){RESET}"
        )

    # ── Schema cross-validation (when live data was used) ────────────────────
    if not r.schema_only and r.schema_keys and not r.fetch_error:
        schema_vs_model_missing = sorted(
            r.schema_keys - r.model_keys - set(KNOWN_SCHEMA_OMISSIONS.get(r.model_name, {}))
        )
        schema_vs_model_extra = sorted(
            r.model_keys - r.schema_keys - set(KNOWN_SCHEMA_EXTRAS.get(r.model_name, {}))
        )
        if schema_vs_model_missing or schema_vs_model_extra:
            print(f"  {DIM}┄ schema cross-check:{RESET}")
            for k in schema_vs_model_missing:
                print(f"    {DIM}schema has {k!r} but model doesn't (consider adding){RESET}")
            for k in schema_vs_model_extra:
                print(f"    {DIM}model has {k!r} but schema doesn't (extension or stale){RESET}")


def _audit_submodels(oa_schema: dict[str, Any]) -> None:
    """Compare pypaperless sub-model classes against OpenAPI schema component fields."""
    schemas = oa_schema.get("components", {}).get("schemas", {})

    print(f"\n{'─' * 70}")
    print(f"  {BOLD}Sub-model Field Audit{RESET}")
    print(f"{'─' * 70}")

    all_ok = True
    for cls, schema_name, usage in SUBMODEL_MAPPINGS:
        schema_component = schemas.get(schema_name, {})
        if not schema_component:
            print(
                f"  {YELLOW}MISSING{RESET}  {cls.__name__}  ↔  {DIM}{schema_name}{RESET}  ({DIM}schema component not found{RESET})"
            )
            all_ok = False
            continue

        schema_keys = set(schema_component.get("properties", {}).keys())
        model_keys = set(cls.model_fields.keys())
        known_omissions = SUBMODEL_KNOWN_OMISSIONS.get(cls.__name__, {})
        known_extras = SUBMODEL_KNOWN_EXTRAS.get(cls.__name__, {})

        raw_missing = sorted(schema_keys - model_keys)
        raw_extra = sorted(model_keys - schema_keys)
        missing = [k for k in raw_missing if k not in known_omissions]
        extra = [k for k in raw_extra if k not in known_extras]
        k_omissions = [k for k in raw_missing if k in known_omissions]
        k_extras = [k for k in raw_extra if k in known_extras]

        if missing or extra:
            all_ok = False
            print(
                f"\n  {RED}GAPS{RESET}   {cls.__name__}  ↔  {DIM}{schema_name}{RESET}  ({DIM}{usage}{RESET})"
            )
            for k in missing:
                print(f"    {RED}✗  schema has {k!r} — missing in {cls.__name__}{RESET}")
            for k in extra:
                print(f"    {YELLOW}⚠  {cls.__name__} has {k!r} — not in schema{RESET}")
        else:
            note = f"{len(schema_keys)} fields"
            if k_omissions or k_extras:
                note += f"  ·  +{len(k_omissions)} omissions, +{len(k_extras)} extras"
            print(
                f"  {GREEN}OK{RESET}     {cls.__name__}  ↔  {DIM}{schema_name}{RESET}  ({DIM}{note}  ·  {usage}{RESET})"
            )

        for k in k_omissions:
            print(f"    {DIM}·  omission: {k!r}  →  {known_omissions[k]}{RESET}")
        for k in k_extras:
            print(f"    {DIM}·  extra:    {k!r}  →  {known_extras[k]}{RESET}")

    if UNSCHEMA_SUBMODELS:
        print(
            f"\n  {CYAN}Sub-models without a direct schema component (field-by-field verification not possible):{RESET}"
        )
        for cls_name, src, usage in UNSCHEMA_SUBMODELS:
            print(f"  {DIM}·  {cls_name:<35s}  ({src})")
            print(f"  {DIM}   └─ {usage}{RESET}")

    if all_ok:
        print(f"\n  {GREEN}{BOLD}All mappable sub-models match schema fields.{RESET}")


def _audit_enums(oa_schema: dict[str, Any]) -> None:
    """Compare pypaperless typed enum classes against OpenAPI schema enum values."""
    schemas = oa_schema.get("components", {}).get("schemas", {})

    print(f"\n{'─' * 70}")
    print(f"  {BOLD}Enum Value Audit{RESET}")
    print(f"{'─' * 70}")

    all_ok = True
    for cls, schema_name, usage in ENUM_MAPPINGS:
        schema_component = schemas.get(schema_name, {})
        if not schema_component:
            print(
                f"  {YELLOW}MISSING{RESET}  {cls.__name__}  ↔  {DIM}{schema_name}{RESET}  ({DIM}schema component not found{RESET})"
            )
            all_ok = False
            continue

        schema_vals: set = set(schema_component.get("enum", []))
        model_vals: set = {m.value for m in cls if m.name != "UNKNOWN"}

        missing = sorted(schema_vals - model_vals, key=str)
        extra = sorted(model_vals - schema_vals, key=str)

        if missing or extra:
            all_ok = False
            print(
                f"\n  {RED}GAPS{RESET}   {cls.__name__}  ↔  {DIM}{schema_name}{RESET}  ({DIM}{usage}{RESET})"
            )
            for v in missing:
                print(f"    {RED}✗  schema has {v!r} — missing in {cls.__name__}{RESET}")
            for v in extra:
                print(f"    {YELLOW}⚠  {cls.__name__} has {v!r} — not in schema{RESET}")
        else:
            print(
                f"  {GREEN}OK{RESET}     {cls.__name__}  ↔  {DIM}{schema_name}{RESET}  ({DIM}{len(schema_vals)} values  ·  {usage}{RESET})"
            )

    if UNTYPED_ENUM_FIELDS:
        print(f"\n  {CYAN}Untyped enum fields (raw int/str; schema has a typed enum):{RESET}")
        for model_name, field_name, schema_name in UNTYPED_ENUM_FIELDS:
            vals = schemas.get(schema_name, {}).get("enum", ["schema not found"])
            print(f"  {DIM}·  {model_name}.{field_name:<35s}  →  {schema_name}: {vals}{RESET}")

    if all_ok:
        print(f"\n  {GREEN}{BOLD}All typed enums match schema values.{RESET}")


def _print_summary(results: list[AuditResult], schema: dict[str, Any]) -> None:
    total = len(results)
    live_ok = sum(
        1
        for r in results
        if not r.fetch_error
        and not r.no_data
        and not r.schema_only
        and not r.missing_in_model
        and not r.extra_in_model
    )
    schema_ok = sum(
        1 for r in results if r.schema_only and not r.missing_in_model and not r.extra_in_model
    )
    gap_count = sum(1 for r in results if r.missing_in_model or r.extra_in_model)
    no_data_count = sum(1 for r in results if r.no_data)
    error_count = sum(1 for r in results if r.fetch_error)

    print(f"\n{'═' * 70}")
    print(f"  {BOLD}Summary{RESET}")
    print(f"{'─' * 70}")
    print(f"  Total endpoints audited  : {total}")
    print(f"  {GREEN}Fully covered (live)     : {live_ok}{RESET}")
    if schema_ok:
        print(f"  {CYAN}Verified via schema only : {schema_ok}{RESET}")
    if gap_count:
        print(f"  {RED}Gaps found               : {gap_count}{RESET}")
    if no_data_count:
        print(f"  {YELLOW}Unverifiable (no data)   : {no_data_count}{RESET}")
    if error_count:
        print(f"  {RED}Fetch errors             : {error_count}{RESET}")

    # Collect all gaps
    all_missing: list[tuple[str, str, bool]] = []
    all_extra: list[tuple[str, str, bool]] = []
    for r in results:
        for k in r.missing_in_model:
            all_missing.append((r.label, k, r.schema_only))
        for k in r.extra_in_model:
            all_extra.append((r.label, k, r.schema_only))

    if all_missing:
        print(f"\n  {RED}{BOLD}Fields missing in model (not captured by any field):{RESET}")
        for label, k, via_schema in all_missing:
            badge = f" {DIM}(schema){RESET}" if via_schema else ""
            print(f"    {RED}✗  {label}.{k}{badge}{RESET}")

    if all_extra:
        print(
            f"\n  {YELLOW}{BOLD}Fields extra in model (declared but reference never returns):{RESET}"
        )
        for label, k, via_schema in all_extra:
            badge = f" {DIM}(schema){RESET}" if via_schema else ""
            print(f"    {YELLOW}⚠  {label}.{k}{badge}{RESET}")

    # ── Unimplemented schema paths ────────────────────────────────────────────
    unimplemented = _unimplemented_paths(schema)
    if unimplemented:
        print(f"\n  {CYAN}{BOLD}Schema paths not covered by const.API_PATH:{RESET}")
        for path, methods in unimplemented:
            print(f"    {CYAN}·  {path:<55s} {DIM}[{methods}]{RESET}")

    print(f"\n{'═' * 70}\n")


