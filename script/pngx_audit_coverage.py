async def main() -> None:
    print(f"\n{BOLD}pypaperless  ·  API coverage audit  (live + OpenAPI schema){RESET}")
    print(f"Target : {PAPERLESS_URL}")
    print(f"{'─' * 70}")

    # ── Fetch OpenAPI schema first ────────────────────────────────────────────
    print("Fetching OpenAPI schema …", end=" ", flush=True)
    try:
        oa_schema = await _fetch_openapi_schema(PAPERLESS_URL, PAPERLESS_TOKEN)
        oa_version = oa_schema.get("info", {}).get("version", "?")
        print(f"{GREEN}OK{RESET}  (server version {oa_version})")
    except Exception as exc:
        print(f"{RED}FAILED{RESET}: {exc}")
        oa_schema = {}

    p = PaperlessClient(
        PAPERLESS_URL,
        PAPERLESS_TOKEN,
        client=httpx.AsyncClient(verify=False),
    )

    results: list[AuditResult] = []

    async with p:
        p.runtime.cache.custom_fields = await p.custom_fields.as_dict()

        for spec in ENDPOINTS:
            try:
                item = await _fetch_item(p, spec)
                s_keys = _schema_keys(oa_schema, spec.schema_component)
                result = _audit_one(spec, item, s_keys)
            except Exception as exc:
                result = AuditResult(label=spec.label, model_name=spec.model_cls.__name__)
                result.fetch_error = str(exc)

            _print_result(result)
            results.append(result)

    _audit_submodels(oa_schema)
    _audit_enums(oa_schema)
    _print_summary(results, oa_schema)


if __name__ == "__main__":
    asyncio.run(main())
