async def _fetch_openapi_schema(url: str, token: str) -> dict[str, Any]:
    """Fetch and return the full OpenAPI schema JSON."""
    async with httpx.AsyncClient(verify=False) as client:
        resp = await client.get(
            f"{url}/api/schema/?format=json",
            headers={"Authorization": f"Token {token}"},
        )
        resp.raise_for_status()
        return resp.json()


def _schema_keys(schema: dict[str, Any], component: str | None) -> set[str]:
    """Return schema property names for a given component, or empty set."""
    if component is None:
        return set()
    return set(
        schema.get("components", {})
        .get("schemas", {})
        .get(component, {})
        .get("properties", {})
        .keys()
    )


def _unimplemented_paths(schema: dict[str, Any]) -> list[tuple[str, str]]:
    """Return (path, http_methods) for schema paths not in const.API_PATH.

    Normalises ``{id}`` placeholders to ``{pk}`` before comparing.
    Excludes utility/action paths from KNOWN_UTILITY_PATHS.
    """

    def _norm(p: str) -> str:
        return re.sub(r"\{[^}]+\}", "{pk}", p)

    known_normalised = {_norm(v) for v in API_PATH.values()}
    result: list[tuple[str, str]] = []
    for path, path_item in sorted(schema["paths"].items()):
        if path in KNOWN_UTILITY_PATHS:
            continue
        if path in INTENTIONALLY_SKIPPED_PATHS:
            continue
        if _norm(path) not in known_normalised:
            methods = ", ".join(
                m.upper() for m in path_item if m in {"get", "post", "put", "patch", "delete"}
            )
            result.append((path, methods))
    return result


