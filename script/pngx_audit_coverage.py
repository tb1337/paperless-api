def _audit_one(spec: EndpointSpec, item: dict[str, Any] | None, s_keys: set[str]) -> AuditResult:
    result = AuditResult(label=spec.label, model_name=spec.model_cls.__name__)

    model_keys = set(spec.model_cls.model_fields.keys())
    result.model_keys = model_keys
    result.schema_keys = s_keys

    # ── No live data – fall back to schema if available ───────────────────────
    if item is None:
        if not s_keys:
            result.no_data = True
            return result

        result.schema_only = True
        ref_keys = s_keys
        omissions = KNOWN_SCHEMA_OMISSIONS.get(spec.model_cls.__name__, {})
        extras = KNOWN_SCHEMA_EXTRAS.get(spec.model_cls.__name__, {})
    else:
        # ── Live data available ───────────────────────────────────────────────
        ref_keys = set(item.keys())
        result.api_keys = ref_keys
        omissions = KNOWN_OMISSIONS.get(spec.model_cls.__name__, {})
        extras = KNOWN_EXTRAS.get(spec.model_cls.__name__, {})

    raw_missing = sorted(ref_keys - model_keys)
    raw_extra = sorted(model_keys - ref_keys)

    for k in raw_missing:
        if k in omissions:
            result.known_omissions.append(k)
        else:
            result.missing_in_model.append(k)

    for k in raw_extra:
        if k in extras:
            result.known_extras.append(k)
        else:
            result.extra_in_model.append(k)

    return result


